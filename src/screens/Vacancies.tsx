                              {c.gaps.map((gap, gi) => (<span key={gi} className="flex items-center gap-1 px-3 py-1.5 bg-error/5 text-error text-[11px] font-bold rounded-lg border border-error/20"><AlertTriangle size={12} />{gap}</span>))}
